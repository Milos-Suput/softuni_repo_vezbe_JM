function lowerOrUpper(char1){
    if('A' <= char1 && char1 <= 'Z'){
        console.log("upper-case");
    } else {
        console.log("lower-case");
    }
}

console.log("ana" < "aaa"); // koja rec bi se u recniku nalazila ispred koje ASCII kod
